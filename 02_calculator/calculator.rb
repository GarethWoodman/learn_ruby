def add(a,b)
    return a + b
end

def subtract(a,b)
    return a - b
end

def sum(numbers)
    sum = 0
    for number in numbers
        sum += number
    end
    return sum
end

def multiply(numbers)
    sum = 1
    for number in numbers
        sum *= number
    end
    return sum
end

def power(a,b)
    return a ** b
end
